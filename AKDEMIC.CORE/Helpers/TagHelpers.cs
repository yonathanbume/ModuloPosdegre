        public ViewContext ViewContext { get; set; }

        public override void Process(TagHelperContext context, TagHelperOutput output)
        {
            base.Process(context, output);

            if (ShouldBeActive())
            {
                MakeActive(output);
            }

            output.Attributes.RemoveAll("is-active-menu");
        }

        private bool ShouldBeActive()
        {
            string currentController = ViewContext.RouteData.Values["Controller"].ToString();
            string currentAction = ViewContext.RouteData.Values["Action"].ToString();
            string currentArea = (ViewContext.RouteData.Values["Area"] != null ? ViewContext.RouteData.Values["Area"].ToString() : "");

            if (!string.IsNullOrWhiteSpace(Controller))
            {
                var list = Controller.Split(',').ToList();

                if (list.Count != 0 && !list.Exists(x => x.ToLower() == currentController.ToLower()))
                {
                    return false;
                }
            }

            //if (!string.IsNullOrWhiteSpace(Controller) && Controller.ToLower() != currentController.ToLower())
            //{
            //    return false;
            //}

            if (!string.IsNullOrWhiteSpace(Action) && Action.ToLower() != currentAction.ToLower())
            {
                return false;
            }

            if (!string.IsNullOrWhiteSpace(Area) && Area.ToLower() != currentArea.ToLower())
            {
                return false;
            }

            foreach (KeyValuePair<string, string> routeValue in RouteValues)
            {
                if (!ViewContext.RouteData.Values.ContainsKey(routeValue.Key) ||
                    ViewContext.RouteData.Values[routeValue.Key].ToString() != routeValue.Value)
                {
                    return false;
                }
            }

            return true;
        }

        private void MakeActive(TagHelperOutput output)
        {
            var classAttr = output.Attributes.FirstOrDefault(a => a.Name == "class");
            if (classAttr == null)
            {
                classAttr = new TagHelperAttribute("class", "m-menu__item--open m-menu__item--expanded");
                output.Attributes.Add(classAttr);
            }
            else if (classAttr.Value == null || classAttr.Value.ToString().IndexOf("m-menu__item--open m-menu__item--expanded") < 0)
            {
                output.Attributes.SetAttribute("class", classAttr.Value == null
                    ? "m-menu__item--open m-menu__item--expanded"
                    : classAttr.Value.ToString() + " m-menu__item--open m-menu__item--expanded");
            }
        }
    }

    public class InputRemoveIdTagHelper : TagHelper
    {
        [HtmlAttributeName("asp-remove-id")]
        public bool IsRemoved { set; get; }

        public override void Process(TagHelperContext context, TagHelperOutput output)
        {
            if (IsRemoved)
            {
                var nameAttrValue = output.Attributes.Single(a => a.Name == "id");
                output.Attributes.Remove(nameAttrValue);
            }

            base.Process(context, output);
        }
    }
